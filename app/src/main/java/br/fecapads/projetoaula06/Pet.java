package br.fecapads.projetoaula06;


public class Pet {
    String nome;
    int idade;
    String cor;
    String raca;
    String aniversario;

    public Pet(String nome, int idade, String cor, String raca, String aniversario) {
        this.nome = nome;
        this.idade = idade;
        this.cor = cor;
        this.raca = raca;
        this.aniversario = aniversario;
    }
}
